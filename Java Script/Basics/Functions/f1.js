// Function let you 
/**
 *  abstract out logic in your program
 * Takes arguments as an input
 * Return a values as an output
 * You can think of them as an independent program that is supposed to do something given an input
 * Functions can take other functions as arguments (callbacks)
 * */

function findSum(a, b){
    return a+b;
}

console.log(findSum(5,6));